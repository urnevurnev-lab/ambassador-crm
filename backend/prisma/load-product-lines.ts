const slugify = (line: string, flavor: string) =>
  `${line}-${flavor}`
    .toUpperCase()
    .replace(/\s+/g, '-')
    .replace(/[()]/g, '')
    .replace(/[^A-Z0-9-Ð-Ð¯Ð-]/g, '');

